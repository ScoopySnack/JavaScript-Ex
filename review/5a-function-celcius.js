function celsiusToFahrenheit(celsius) {
  return (celsius * 9 / 5) + 32      //This the basic syntx of a function. 
}

//They are callable by name, and they can take arguments.
//They can also return values, which can be used later in the program.
//They can be defined using the function keyword, followed by the function name, a list of parameters, and the function body enclosed in curly braces.
//The function body contains the statements that are executed when the function is called.
//The return statement is used to specify the value that the function should return when it is called.

console.log(celsiusToFahrenheit(0)); // 32
//The console.log() function is used to print the result of the celsiusToFahrenheit function to the console.